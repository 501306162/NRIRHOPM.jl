@testset "neighbor" begin
    @testset "8-connected neighborhood" begin
        # test for simplest 8-connected neighborhood
        # coordinate system(r,c):
        #  up  r     c --->
        #  to  |   left to right
        # down ↓
        # coordinate => point => label:
        # 𝒊 => p1 => α   𝒋 => p2 => β   𝒌 => p3 => χ

        # pairwise-cliques(p1,p2)
        #  1  4  7       11  12  13
        #  2  5  8  <=>  21  22  23
        #  3  6  9       31  32  33
        index = @inferred neighbors(C8Pairwise(), (3,3))

        pixel11 = []
        pixel21 = [(2,1)]
        pixel31 = [(3,2)]
        pixel12 = [(4,1), (4,2)]
        pixel22 = [(5,1), (5,2), (5,3), (5,4)]
        pixel32 = [(6,2), (6,3), (6,5)]
        pixel13 = [(7,4), (7,5)]
        pixel23 = [(8,4), (8,5), (8,6), (8,7)]
        pixel33 = [(9,5), (9,6), (9,8)]

        @test index == [pixel11; pixel21; pixel31; pixel12; pixel22; pixel32; pixel13; pixel23; pixel33]

        # trey-cliques(p1,p2,p3)
        #  1  4  7       11  12  13
        #  2  5  8  <=>  21  22  23
        #  3  6  9       31  32  33
        ◸, ◺, ◹, ◿ = @inferred neighbors(C8Topology(), (3,3))

        @test ◸ == [(1,2,4), (2,3,5), (4,5,7), (5,6,8)]
        @test ◺ == [(2,1,5), (3,2,6), (5,4,8), (6,5,9)]
        @test ◹ == [(4,5,1), (5,6,2), (7,8,4), (8,9,5)]
        @test ◿ == [(5,4,2), (6,5,3), (8,7,5), (9,8,6)]
    end

    @testset "26-connected neighborhood" begin
        # coordinate system(r,c,z):
        #  up  r     c --->        z × × (front to back)
        #  to  |   left to right     × ×
        # down ↓
        # coordinate => point => label:
        # 𝒊 => p1 => α   𝒋 => p2 => β   𝒌 => p3 => χ   𝒎 => p5 => δ

        # pairwise-cliques(p1,p2)
        #  1  4  7        111  121  131
        #  2  5  8   <=>  211  221  231
        #  3  6  9        311  321  331
        #-----------front--------------
        # 10 13 16        112  122  132
        # 11 14 17   <=>  212  222  232
        # 12 15 18        312  322  332
        #-----------middle-------------
        # 19 22 25        113  123  133
        # 20 23 26   <=>  213  223  233
        # 21 24 27        313  323  333
        #-----------back---------------
        index = @inferred neighbors(C26Pairwise(), (3,3,3))

        voxel111 = []
        voxel211 = [(2,1)]
        voxel311 = [(3,2)]
        voxel121 = [(4,1), (4,2)]
        voxel221 = [(5,1), (5,2), (5,3), (5,4)]
        voxel321 = [(6,2), (6,3), (6,5)]
        voxel131 = [(7,4), (7,5)]
        voxel231 = [(8,4), (8,5), (8,6), (8,7)]
        voxel331 = [(9,5), (9,6), (9,8)]

        voxel112 = [(10,1), (10,2), (10,4), (10,5)]
        voxel212 = [(11,1), (11,2), (11,3), (11,4), (11,5), (11,6), (11,10)]
        voxel312 = [(12,2), (12,3), (12,5), (12,6), (12,11)]
        voxel122 = [(13,1), (13,2), (13,4), (13,5), (13,7), (13,8), (13,10), (13,11)]
        voxel222 = [(14,1), (14,2), (14,3), (14,4), (14,5), (14,6), (14,7), (14,8), (14,9), (14,10), (14,11), (14,12), (14,13)]
        voxel322 = [(15,2), (15,3), (15,5), (15,6), (15,8), (15,9), (15,11), (15,12), (15,14)]
        voxel132 = [(16,4), (16,5), (16,7), (16,8), (16,13), (16,14)]
        voxel232 = [(17,4), (17,5), (17,6), (17,7), (17,8), (17,9), (17,13), (17,14), (17,15), (17,16)]
        voxel332 = [(18,5), (18,6), (18,8), (18,9), (18,14), (18,15), (18,17)]

        voxel113 = [(19,10), (19,11), (19,13), (19,14)]
        voxel213 = [(20,10), (20,11), (20,12), (20,13), (20,14), (20,15), (20,19)]
        voxel313 = [(21,11), (21,12), (21,14), (21,15), (21,20)]
        voxel123 = [(22,10), (22,11), (22,13), (22,14), (22,16), (22,17), (22,19), (22,20)]
        voxel223 = [(23,10), (23,11), (23,12), (23,13), (23,14), (23,15), (23,16), (23,17), (23,18), (23,19), (23,20), (23,21), (23,22)]
        voxel323 = [(24,11), (24,12), (24,14), (24,15), (24,17), (24,18), (24,20), (24,21), (24,23)]
        voxel133 = [(25,13), (25,14), (25,16), (25,17), (25,22), (25,23)]
        voxel233 = [(26,13), (26,14), (26,15), (26,16), (26,17), (26,18), (26,22), (26,23), (26,24), (26,25)]
        voxel333 = [(27,14), (27,15), (27,17), (27,18), (27,23), (27,24), (27,26)]

        @test index == [voxel111; voxel211; voxel311; voxel121; voxel221; voxel321; voxel131; voxel231; voxel331;
                        voxel112; voxel212; voxel312; voxel122; voxel222; voxel322; voxel132; voxel232; voxel332;
                        voxel113; voxel213; voxel313; voxel123; voxel223; voxel323; voxel133; voxel233; voxel333]

        # quadra-cliques (p1,p2,p3,p5)
        #  1  4  7        111  121  131
        #  2  5  8   <=>  211  221  231
        #  3  6  9        311  321  331
        #-----------front--------------
        # 10 13 16        112  122  132
        # 11 14 17   <=>  212  222  232
        # 12 15 18        312  322  332
        #-----------middle-------------
        # 19 22 25        113  123  133
        # 20 23 26   <=>  213  223  233
        # 21 24 27        313  323  333
        #-----------back---------------
        fff, bff, fbf, bbf, ffb, bfb, fbb, bbb = @inferred neighbors(C26Topology(), (3,3,3))

        @test fff == [(1,2,4,10), (2,3,5,11), (4,5,7,13), (5,6,8,14),
                      (10,11,13,19), (11,12,14,20), (13,14,16,22), (14,15,17,23)]

        @test bff == [(2,1,5,11), (3,2,6,12), (5,4,8,14), (6,5,9,15),
                      (11,10,14,20), (12,11,15,21), (14,13,17,23), (15,14,18,24)]

        @test fbf == [(4,5,1,13), (5,6,2,14), (7,8,4,16), (8,9,5,17),
                      (13,14,10,22), (14,15,11,23), (16,17,13,25), (17,18,14,26)]

        @test bbf == [(5,4,2,14), (6,5,3,15), (8,7,5,17), (9,8,6,18),
                      (14,13,11,23), (15,14,12,24), (17,16,14,26), (18,17,15,27)]

        @test ffb == [(10,11,13,1), (11,12,14,2), (13,14,16,4), (14,15,17,5),
                      (19,20,22,10), (20,21,23,11), (22,23,25,13), (23,24,26,14)]

        @test bfb == [(11,10,14,2), (12,11,15,3), (14,13,17,5), (15,14,18,6),
                      (20,19,23,11), (21,20,24,12), (23,22,26,14), (24,23,27,15)]

        @test fbb == [(13,14,10,4), (14,15,11,5), (16,17,13,7), (17,18,14,8),
                      (22,23,19,13), (23,24,20,14), (25,26,22,16), (26,27,23,17)]

        @test bbb == [(14,13,11,5), (15,14,12,6), (17,16,14,8), (18,17,15,9),
                      (23,22,20,14), (24,23,21,15), (26,25,23,17), (27,26,24,18)]
    end
end
